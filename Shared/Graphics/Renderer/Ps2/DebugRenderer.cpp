void DebugRenderer::initImage()
{
	// this is just temporary until proper texturemanager is in.

	u8* memory = m_fontTexture;

	memory += 4;	
	u16 palRelocCount = (u16)(*(u16*)memory); memory += 2;	
	u16 imageRelocCount = (u16)(*(u16*)memory); memory += 2;

	u32* palRelocList = (u32*)memory; memory += (4+4) * palRelocCount;	
	u32* imageRelocList = (u32*)memory; memory += (4+4) * imageRelocCount;	
	m_callChains[0] = memory = reinterpret_cast<u8*>(alignedAdress(memory, 16));	
	memory += 2048;	// fixed size of callchain	
	m_callChains[1] = memory;	
	memory += 2048;	// fixed size of callchain	

	u32 totalSize = (u32)(*(u32*)memory);	
	//u32 palSize = (u32)(*(u32*)(memory+4));

	memory += 8;
	memory = m_imageData = reinterpret_cast<u8*>(alignedAdress(memory, 16));
	memory += totalSize;
	memory = m_palData = reinterpret_cast<u8*>(alignedAdress(memory, 16));

	// relloc the callchain

	for (uint i = 0; i < palRelocCount; ++i)
	{
		u32* callOffset = (u32*)(m_callChains[0] + palRelocList[0]+4);
		*callOffset += palRelocList[1] + u32(m_palData);
		callOffset = (u32*)(m_callChains[1] + palRelocList[0]+4);
		*callOffset += palRelocList[1] + u32(m_palData);

		palRelocList += 2;
	}

	for (uint i = 0; i < imageRelocCount; ++i)
	{
		u32* callOffset = (u32*)(m_callChains[0] + imageRelocList[0]+4);
		*callOffset += imageRelocList[1] + u32(m_imageData);
		callOffset = (u32*)(m_callChains[1] + imageRelocList[0]+4);
		*callOffset += imageRelocList[1] + u32(m_imageData);

		imageRelocList += 2;
	}
}

