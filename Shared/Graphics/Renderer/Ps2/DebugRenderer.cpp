void DebugRenderer::text(const char* dispText, float x, float y, const Color32& color)
{
	if (!dispText)
		return;

	// we should rewrite this when proper texture manager is in.

	m_dmaChain.addSrcCallTag(m_callChains[0]);
	
	uint length = strlen(dispText); 

	m_dmaChain.addSrcCntTag(0,VIF_DIRECT((length*4) + 3 + 3));

	m_dmaChain.add128(GIFTAG(4, gif::Eop, 0, 0, gif::Packed, 1), gif::A_D);
	m_dmaChain.add128(GS_TEST(1, 0, 0, 1, 0, 0, 1, 1),GS_REG(TEST_1));
	m_dmaChain.add128(GS_ALPHA(0, 2, 2, 1, 0x80),GS_REG(ALPHA_1));
	m_dmaChain.add128(GS_RGBAQ(255, 255, 255, 127, 1.0f),GS_REG( RGBAQ ));
	m_dmaChain.add128(setTex0Pal((450*32)+4, (450*32), 2, 20, 7, 7, 0, 1), GS_REG(TEX0_1));

	m_dmaChain.add64(GIFTAG(length , gif::Eop, gif::Pre, GS_PRIM(gs::Sprite, gs::Flat, gs::Texturing, 0, 1, 0,
								gs::Stq, gs::Context1, 0), gif::Packed, 4));
	m_dmaChain.add64(0x5252);

	uint offsetX = 2048 - 640/2;
	uint offsetY = 2048 - 224/2;

	x *= 640.0f;
	y *= 224.0f;

	uint x0 = uint(x) + offsetX;
	uint y0 = uint(y) + offsetY;

	uint x1 = uint(x) + offsetX + 10;
	uint y1 = uint(y) + offsetY + 14;

	for (uint i = 0; i < length; ++i)
	{
		float s0, t0;

		findRow(dispText[i], s0, t0);

		float s1 = s0 + 9.0f;
		float t1 = t0 + 14.0f;

		m_dmaChain.addFloat(s0 / 128.0f);
		m_dmaChain.addFloat(t0 / 128.0f); 
		m_dmaChain.add64(0);
		m_dmaChain.add32(x0 * 16); 
		m_dmaChain.add32(y0 * 16); 
		m_dmaChain.add64(0);

		m_dmaChain.addFloat(s1 / 128.0f);
		m_dmaChain.addFloat(t1 / 128.0f); 
		m_dmaChain.add64(0);
		m_dmaChain.add32(x1 * 16); 
		m_dmaChain.add32(y1 * 16); 
		m_dmaChain.add64(0);

		x0 += 9;
		x1 += 9;
	}

	m_dmaChain.endPacket();

	m_dmaChain.addSrcCntTag(0, VIF_DIRECT(2));
	m_dmaChain.add128(GIFTAG(1, gif::Eop, 0, 0, gif::Packed, 1), gif::A_D);
	m_dmaChain.add128(GS_TEST(1, 7, 0xFF, 0, 0, 0, 1, 2),GS_REG(TEST_1));
	m_dmaChain.endPacket();
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

u64 DebugRenderer::setTex0Pal(int tbp0, int cbp, int tbw, int psm, int tw, int th, int tcc, int tfx)
{
	return u64(tbp0 + (tbw << 14) + (psm << 20) + (tw << 26) +
		      ((u64)th << 30) + ((u64)tcc << 34) + ((u64)tfx << 35) + ((u64)cbp << 37) + ((u64)0 << 55));
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DebugRenderer::findRow(char c, float& x, float& y)
{
	char rowCount = 14;

	// ugly bugfix because i have missed " ' "

	if (c > 96)
		c--;

	// we could use a table but what the hell..

	c -= 32;

	for (uint i = 0; i < 7; ++i)
	{
		if (c < rowCount)
		{
			y = i * 14;
			x = (c - (i * 14)) * 9;
			return;
		}
		
		rowCount += 14;
	}

	x = 0;
	y = 0;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DebugRenderer::begin()
{
	ZENIC_ASSERT_DESC(!m_begin, "You must call end after the previous rendering");

	m_camera.addToChain();	

	Vu1::instance()->clearCache();
#ifndef PS2_EMU
//	m_vuProgram = Vu1::instance()->getProgram(m_dmaChain, 1, DebugRendererVu1_CodeStart, DebugRendererVu1_CodeEnd);
//#else
	m_vuProgram = Vu1::instance()->getProgram(m_dmaChain, 1, &DebugRendererVu1_CodeStart, &DebugRendererVu1_CodeEnd);
#endif

	m_dmaChain.addSrcCntTag(VIF_STCYCL(1,1),VIF_UNPACK(vif::V4_32, 2,  20));

	m_dmaChain.add64(GIFTAG(0, 0, gif::Pre, GS_PRIM(gs::Linestrip, gs::Gouraud, 0, 0, 0, 0, gs::Stq, gs::Context1, 0), 
						  gif::Packed, 2));
	m_dmaChain.add64(0x51);

	m_dmaChain.add64(GIFTAG(0, 0, gif::Pre, GS_PRIM(gs::Line, gs::Gouraud, 0, 0, 0, 0, gs::Stq, gs::Context1, 0),
						  gif::Packed, 2));
	m_dmaChain.add64(0x51);

	m_dmaChain.add32(VIF_BASE(22));
	m_dmaChain.add32(VIF_OFFSET(523));
	m_dmaChain.add32(VIF_STMOD(0));
	m_dmaChain.add32(VIF_STCYCL(1, 1));

	m_dmaChain.add32(VIF_MSCAL(m_vuProgram));
	m_dmaChain.add32(0);
	m_dmaChain.add32(0);
	m_dmaChain.add32(0);
	m_dmaChain.endPacket();

	m_begin = true;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DebugRenderer::end()
{
	ZENIC_ASSERT(m_begin);
	m_begin = false;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DebugRenderer::line(const Vector3& p0, const Vector3& p1, const Color32& color)
{
	triangleInternal(p0, p1, p1 + Vector3(0.001f, 0.001f, 0.001f), color, 1);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DebugRenderer::triangle(const Vector3& p1, const Vector3& p2, const Vector3& p3, const Color32& color)
{
	triangleInternal(Vector3(0,0,0), Vector3(0,0,0), Vector3(0,0,0), color, 1);
	triangleInternal(p1, p2, p3, color, 0);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DebugRenderer::triangleInternal(const Vector3& p1, const Vector3& p2, const Vector3& p3, const Color32& color,
									   uint /*primType*/)
{
	ZENIC_ASSERT_DESC(m_begin, "You must call begin before drawing any triangles");

	m_dmaChain.addSrcCntTag(VIF_STCYCL(1, 1), VIF_UNPACKR(vif::V4_32, 4, 0)); 

	// color

	m_dmaChain.add32(color.r);
	m_dmaChain.add32(color.g);
	m_dmaChain.add32(color.b);
	m_dmaChain.add32(color.a);

	// coords

	m_dmaChain.addFloat(p1.x);
	m_dmaChain.addFloat(p1.y);
	m_dmaChain.addFloat(p1.z);
	m_dmaChain.addFloat(1.0f);

	m_dmaChain.addFloat(p2.x);
	m_dmaChain.addFloat(p2.y);
	m_dmaChain.addFloat(p2.z);
	m_dmaChain.addFloat(1.0f);

	m_dmaChain.addFloat(p3.x);
	m_dmaChain.addFloat(p3.y);
	m_dmaChain.addFloat(p3.z);
	m_dmaChain.addFloat(1.0f);

	m_dmaChain.add32(VIF_FLUSH());
	m_dmaChain.add32(VIF_MSCNT());
	m_dmaChain.add32(0);
	m_dmaChain.add32(0);

	m_dmaChain.endPacket();
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

inline void* alignedAdress(void* adress, const uint& alignment)
{
	u8* tempAdress = reinterpret_cast<u8*>(adress);

	while(uint(tempAdress) & (alignment-1))
		tempAdress++;

	return tempAdress;
}

