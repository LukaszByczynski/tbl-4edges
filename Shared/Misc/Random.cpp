uint Random::m_seed = 93186752;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

float Random::rand()
{
	static uint a = 1588635695, m = 4294967291U, q = 2, r = 1117695901;
	m_seed = a*(m_seed % q) - r*(m_seed / q);
	return ((float)m_seed / (float)m);
}

